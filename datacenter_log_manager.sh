menu_logs() {
    while option=$(zenity \
        --title "$APP_TITLE — Log Management" \
        --height=560 --width=400 \
        --list --text="Select an action:" \
        --column="" --column="Option" --radiolist \
        TRUE  "Collect logs from single node" \
        FALSE "Collect logs from all nodes" \
        FALSE "Upload staged logs to FTP" \
        FALSE "Collect & Upload all nodes" \
        FALSE "Tail a log file"); do

        case "$option" in
            "Collect logs from single node") collect_single_node_logs_menu ;;
            "Collect logs from all nodes")   collect_all_nodes_logs_menu ;;
            "Upload staged logs to FTP")     upload_ticket_logs_menu ;;
            "Collect & Upload all nodes")    collect_and_upload_all ;;
            "Tail a log file")               tail_log ;;
            *) exit ;;
        esac
    done
}

menu_nodes() {
    while option=$(zenity \
        --title "$APP_TITLE — Node Management" \
        --height=440 --width=280 \
        --list --text="Select an action:" \
        --column="" --column="Option" --radiolist \
        TRUE  "Ping a node" \
        FALSE "Check all nodes connectivity" \
        FALSE "Show node hardware info" \
        FALSE "Scan all nodes info"); do

        case "$option" in
            "Ping a node")                  ping_node ;;
            "Check all nodes connectivity") check_all_nodes ;;
            "Show node hardware info")      show_node_info ;;
            "Scan all nodes info")          scan_all_nodes_info ;;
            *) exit ;;
        esac
    done
}

menu_services() {
    while option=$(zenity \
        --title "$APP_TITLE — Service Management" \
        --height=200 --width=350 \
        --list --text="Select an action:" \
        --column="" --column="Option" --radiolist \
        TRUE  "Verify and start all services" \
        FALSE "Restart a single service"); do

        case "$option" in
            "Verify and start all services") verify_and_start_services ;;
            "Restart a single service")      restart_single_service ;;
            *) exit ;;
        esac
    done
}


