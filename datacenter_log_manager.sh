# Shows hardware/OS info for a single node via SSH
show_node_info() {
    local node
    node=$(zenity --entry \
        --title="$APP_TITLE — Node Info" \
        --text="Node name or IP:" \
        --entry-text="${NODE_PREFIX}1") || return

    local info_file
    info_file=$(mktemp)

    (
        echo "20"; echo "# Connecting to $node..."
        {
            echo "=== Node: $node — $(date) ==="
            echo ""
            ssh -o ConnectTimeout=10 -o BatchMode=yes \
                "${SSH_USER}@${node}" \
                "echo '--- Hostname ---'; hostname -f; \
                 echo '--- Uptime ---'; uptime; \
                 echo '--- OS ---'; cat /etc/os-release 2>/dev/null | head -3; \
                 echo '--- CPU ---'; lscpu | grep 'Model name\|CPU(s)\|Thread'; \
                 echo '--- Memory ---'; free -h; \
                 echo '--- Disk ---'; df -h / 2>/dev/null; \
                 echo '--- Network ---'; ip -br addr 2>/dev/null || ifconfig 2>/dev/null" 2>&1
        } >> "$info_file"
        echo "100"; echo "# Done"
    ) | zenity --progress \
        --title="$APP_TITLE" \
        --text="Gathering info from $node..." \
        --percentage=0 --auto-close --auto-kill

    zenity --text-info \
        --title="$APP_TITLE — $node Information" \
        --width=600 --height=500 \
        --filename="$info_file"

    rm -f "$info_file"
}

# Scans all nodes and reports basic reachability + hostname
scan_all_nodes_info() {
    local report_file
    report_file=$(mktemp)
    local total=$(( NODE_END - NODE_START + 1 ))
    local current=0

    (
        echo "=== Node Scan Report — $(date) ===" >> "$report_file"
        echo "" >> "$report_file"

        for i in $(seq "$NODE_START" "$NODE_END"); do
            local node="${NODE_PREFIX}${i}"
            current=$(( current + 1 ))
            echo "$(( current * 100 / total ))"
            echo "# Scanning $node..."

            if ping -c 1 -W 2 "$node" &>/dev/null; then
                local hostname uptime_info
                hostname=$(ssh -o ConnectTimeout=5 -o BatchMode=yes \
                    "${SSH_USER}@${node}" "hostname -f" 2>/dev/null || echo "SSH unavailable")
                uptime_info=$(ssh -o ConnectTimeout=5 -o BatchMode=yes \
                    "${SSH_USER}@${node}" "uptime -p" 2>/dev/null || echo "N/A")
                echo "✔  $node | $hostname | $uptime_info" >> "$report_file"
            else
                echo "✘  $node | UNREACHABLE" >> "$report_file"
            fi
        done
        echo "100"; echo "# Scan complete"
    ) | zenity --progress \
        --title="$APP_TITLE — Scanning Nodes" \
        --text="Starting scan..." \
        --percentage=0 --auto-close --auto-kill

    zenity --text-info \
        --title="$APP_TITLE — Node Scan Report" \
        --width=650 --height=450 \
        --filename="$report_file"

    rm -f "$report_file"
}


